<?php

use App\Models\Movement;
use App\Models\MovementLine;
use App\Models\Sku;
use App\Models\User;
use App\Models\Warehouse;
use App\Services\Repair\RepairOrderService;

beforeEach(function () {
    $this->user = User::factory()->create();

    $this->central = Warehouse::factory()->create(['code' => 'ALM', 'type' => 'central']);
    $this->store = Warehouse::factory()->create(['code' => 'TDA1', 'type' => 'store']);
    $this->workshop = Warehouse::factory()->create(['code' => 'TALLER', 'type' => 'workshop']);
    $this->scrap = Warehouse::factory()->create(['code' => 'MERMA', 'type' => 'scrap']);

    $this->service = app(RepairOrderService::class);
});

/**
 * Helper: agrega N unidades de un SKU al taller mediante un movement
 * confirmado. Imita lo que haría el atajo "Reportar dañado".
 */
function placeInWorkshop(Sku $sku, Warehouse $workshop, float $qty, User $user): void
{
    $movement = Movement::factory()->confirmed()->create([
        'type' => 'transfer',
        'origin_warehouse_id' => null,
        'destination_warehouse_id' => $workshop->id,
        'created_by' => $user->id,
        'confirmed_by' => $user->id,
    ]);

    MovementLine::create([
        'movement_id' => $movement->id,
        'sku_id' => $sku->id,
        'warehouse_id' => $workshop->id,
        'direction' => 'in',
        'quantity' => $qty,
    ]);
}

it('availableForRepair returns workshop stock minus claimed by open orders', function () {
    $sku = Sku::factory()->create();
    placeInWorkshop($sku, $this->workshop, 10, $this->user);

    expect($this->service->availableForRepair($sku, $this->workshop))->toBe(10.0);

    // Open order claims 4 units → only 6 available.
    $this->service->open($this->user, [
        ['sku_id' => $sku->id, 'quantity_claimed' => 4],
    ]);

    expect($this->service->availableForRepair($sku, $this->workshop))->toBe(6.0);
});

it('open rejects claims that exceed the available stock', function () {
    $sku = Sku::factory()->create();
    placeInWorkshop($sku, $this->workshop, 3, $this->user);

    expect(fn () => $this->service->open($this->user, [
        ['sku_id' => $sku->id, 'quantity_claimed' => 5],
    ]))->toThrow(RuntimeException::class, 'Solo hay');
});

it('open rejects double-claim that would exceed available stock', function () {
    $sku = Sku::factory()->create();
    placeInWorkshop($sku, $this->workshop, 5, $this->user);

    $this->service->open($this->user, [
        ['sku_id' => $sku->id, 'quantity_claimed' => 4],
    ]);

    // Solo queda 1 disponible. Una segunda orden por 2 debe fallar.
    expect(fn () => $this->service->open($this->user, [
        ['sku_id' => $sku->id, 'quantity_claimed' => 2],
    ]))->toThrow(RuntimeException::class, 'Solo hay');
});

it('open creates the order and lines with the unique code', function () {
    $sku = Sku::factory()->create();
    placeInWorkshop($sku, $this->workshop, 8, $this->user);

    $order = $this->service->open(
        user: $this->user,
        linesData: [['sku_id' => $sku->id, 'quantity_claimed' => 3]],
        notes: 'Patas dobladas',
    );

    expect($order->code)->toStartWith('REP-')
        ->and($order->status)->toBe('open')
        ->and($order->notes)->toBe('Patas dobladas')
        ->and($order->lines)->toHaveCount(1)
        ->and((float) $order->lines->first()->quantity_claimed)->toBe(3.0);
});

it('close generates transfers to destination and scrap, then closes the order', function () {
    $sku = Sku::factory()->create();
    placeInWorkshop($sku, $this->workshop, 10, $this->user);

    $order = $this->service->open($this->user, [
        ['sku_id' => $sku->id, 'quantity_claimed' => 5],
    ]);

    $line = $order->lines->first();

    $this->service->close($order, $this->user, [
        [
            'line_id' => $line->id,
            'quantity_repaired' => 3,
            'quantity_scrapped' => 2,
            'destination_warehouse_id' => $this->central->id,
        ],
    ]);

    $order->refresh();

    expect($order->status)->toBe('closed')
        ->and($order->outcome)->toBe('completed');

    // Movements generated by closing: one to ALM (3) + one to MERMA (2).
    $movs = Movement::where('reference_type', 'repair_order')
        ->where('reference_id', $order->id)
        ->get();

    expect($movs)->toHaveCount(2);

    // Stock final: TALLER=10-3-2=5, ALM=3, MERMA=2.
    expect($sku->stockAt($this->workshop->id))->toBe(5.0)
        ->and($sku->stockAt($this->central->id))->toBe(3.0)
        ->and($sku->stockAt($this->scrap->id))->toBe(2.0);
});

it('close rejects when repaired plus scrapped does not equal claimed', function () {
    $sku = Sku::factory()->create();
    placeInWorkshop($sku, $this->workshop, 5, $this->user);

    $order = $this->service->open($this->user, [
        ['sku_id' => $sku->id, 'quantity_claimed' => 5],
    ]);
    $line = $order->lines->first();

    expect(fn () => $this->service->close($order, $this->user, [
        [
            'line_id' => $line->id,
            'quantity_repaired' => 2,
            'quantity_scrapped' => 1,
            'destination_warehouse_id' => $this->central->id,
        ],
    ]))->toThrow(RuntimeException::class, 'debe igualar');
});

it('close rejects repaired without destination warehouse', function () {
    $sku = Sku::factory()->create();
    placeInWorkshop($sku, $this->workshop, 5, $this->user);

    $order = $this->service->open($this->user, [
        ['sku_id' => $sku->id, 'quantity_claimed' => 5],
    ]);
    $line = $order->lines->first();

    expect(fn () => $this->service->close($order, $this->user, [
        [
            'line_id' => $line->id,
            'quantity_repaired' => 4,
            'quantity_scrapped' => 1,
            'destination_warehouse_id' => null,
        ],
    ]))->toThrow(RuntimeException::class, 'Falta almacén destino');
});

it('cancel closes the order without generating movements and frees the stock', function () {
    $sku = Sku::factory()->create();
    placeInWorkshop($sku, $this->workshop, 5, $this->user);

    $order = $this->service->open($this->user, [
        ['sku_id' => $sku->id, 'quantity_claimed' => 4],
    ]);

    expect($this->service->availableForRepair($sku, $this->workshop))->toBe(1.0);

    $this->service->cancel($order, $this->user, 'Equivocación al abrir');
    $order->refresh();

    expect($order->status)->toBe('closed')
        ->and($order->outcome)->toBe('cancelled')
        ->and($order->notes)->toContain('Cancelada');

    // No movements generated.
    expect(Movement::where('reference_type', 'repair_order')->count())->toBe(0);

    // Stock vuelve a estar disponible (5 unidades, ya nada reclamado).
    expect($this->service->availableForRepair($sku, $this->workshop))->toBe(5.0);
});

it('close rejects when order is already closed', function () {
    $sku = Sku::factory()->create();
    placeInWorkshop($sku, $this->workshop, 5, $this->user);

    $order = $this->service->open($this->user, [
        ['sku_id' => $sku->id, 'quantity_claimed' => 5],
    ]);
    $line = $order->lines->first();

    $this->service->close($order, $this->user, [
        [
            'line_id' => $line->id,
            'quantity_repaired' => 5,
            'quantity_scrapped' => 0,
            'destination_warehouse_id' => $this->central->id,
        ],
    ]);

    expect(fn () => $this->service->cancel($order->fresh(), $this->user))
        ->toThrow(RuntimeException::class, 'ya está cerrada');
});

it('repairableSkus returns only SKUs with available workshop stock', function () {
    $skuWith = Sku::factory()->create();
    $skuClaimed = Sku::factory()->create();
    $skuNone = Sku::factory()->create();

    placeInWorkshop($skuWith, $this->workshop, 4, $this->user);
    placeInWorkshop($skuClaimed, $this->workshop, 2, $this->user);

    // Reclamamos todo el stock de skuClaimed.
    $this->service->open($this->user, [
        ['sku_id' => $skuClaimed->id, 'quantity_claimed' => 2],
    ]);

    $repairable = $this->service->repairableSkus();

    expect($repairable->pluck('id')->all())
        ->toContain($skuWith->id)
        ->not->toContain($skuClaimed->id)
        ->not->toContain($skuNone->id);
});
